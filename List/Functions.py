def run():

    nums = [3, 41, 12, 9, 74, 15]

    print("La longitud de la lista es de " +  str(len(nums)))

    print("El número máximo de la lista es " +  str(max(nums)))

    print("El número mínimo de la lista es " +  str(min(nums)))

    print("La suma total de la lista es " +  str(sum(nums)))

if __name__ == "__main__":
    run()